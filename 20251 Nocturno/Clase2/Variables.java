public class Variables {
    public static void main(String[] args) {
        
        //Declaración de una variable de tipo entero.
        int edad = 25;
        
        //Declaración de una variable de tipo real.
        double altura = 1.75;
        
        //Declaración de una variable de Cadena.
        String nombre = "Juan";
        
        //Declaración de una variable de tipo boolean.
        boolean esEstudiante = true;
        
        /*
        Con la instrucción System.out.println se mostraran
        las variables y cada una de ellas estará concatenada con el signo (+)
        y la leyenda. 
        */

        System.out.println("Nombre: " + nombre + ", Edad: " + edad + ", Altura: " + altura);
        System.out.println("Estudiante: " + esEstudiante);
    }
}
